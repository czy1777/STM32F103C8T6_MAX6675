/**
 * @brief  自定义 printf 函数，通过 UART 发送格式化字符串
 * 
 * @param  huart        UART 外设句柄
 * @param  format       格式化字符串模板
 * @param  ...          可变参数列表（与格式化字符串匹配的实际值）
 * 
 * @return 发送的字节数
 */
int my_printf(UART_HandleTypeDef *huart, const char *format, ...)
{
    // 定义 512 字节区缓冲存储格式化后的字符串
    char buffer[512]; 
    
    // 初始化可变参数列表
    va_list arg;   
    int len;         
    
    // 开始处理可变参数列表
    va_start(arg, format);
    
    // 格式化字符串到缓冲区（带缓冲区大小检查防止溢出）
    len = vsnprintf(buffer, sizeof(buffer), format, arg);
    
    // 结束可变参数列表处理
    va_end(arg);
    
    // 通过 UART 发送缓冲区数据
    HAL_UART_Transmit(huart, (uint8_t *)buffer, (uint16_t)len, 0xFF);
    
    // 返回实际发送的字节数
    return len;
}

